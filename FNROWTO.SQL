-- =============================================
-- FIND THE ENDING ROW UNTIL WHICH THE RECORDS HAS TO BE RETURNED
-- USED IN QUERIES WHERE RECORDS ARE RETURNED IN BATCHES
-- =============================================
CREATE FUNCTION FNROWTO
(
	@PAGENUMBER INT,
	@PAGESIZE INT
)
RETURNS BIGINT
WITH RETURNS NULL ON NULL INPUT
--,ENCRYPTION
AS
BEGIN
	RETURN (@PAGENUMBER * @PAGESIZE)
END
GO
