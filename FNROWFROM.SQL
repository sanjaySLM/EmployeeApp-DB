-- =============================================
-- FIND THE STARTING ROW FROM WHICH THE RECORDS HAS TO BE RETURNED
-- USED IN QUERIES WHERE RECORDS ARE RETURNED IN BATCHES
-- =============================================
CREATE FUNCTION FNROWFROM
(
	@PAGENUMBER INT,
	@PAGESIZE INT
)
RETURNS BIGINT
WITH RETURNS NULL ON NULL INPUT
--,ENCRYPTION
AS
BEGIN
	RETURN (@PAGENUMBER * @PAGESIZE) - @PAGESIZE + 1
END



GO
