-- =============================================
-- FIND THE TOTAL NUMBER OF PAGES RETURNED FOR BATCH QUERY
-- USED IN SPS WHERE RECORDS ARE RETURNED IN BATCHES
-- =============================================
CREATE FUNCTION FNTOTALPAGECOUNT
(
	@RESULTCOUNT INT,
	@PAGESIZE INT
)
RETURNS INT
WITH RETURNS NULL ON NULL INPUT
--,ENCRYPTION
AS
BEGIN
	RETURN CEILING(CONVERT(REAL,@RESULTCOUNT) / @PAGESIZE)
END



GO
